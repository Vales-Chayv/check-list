// Принцип: КЛЮЧ = русский текст.
//   t('Добавить карточку') → если для текущего языка есть перевод, вернёт его,
//   иначе вернёт сам ключ (русский). Поэтому словарь 'ru' не нужен.
//   Словари en/he заполняем позже, по мере перевода.

let currentLang = localStorage.getItem('mc_lang') || 'ru';

const I18N = {
  en: {},
  he: {}
};

// Перевести строку. Нет перевода → показываем русский (ключ).
function t(s) {
  const dict = I18N[currentLang];
  return (dict && dict[s]) || s;
}

// Сменить язык (сохраняется между сессиями). Перерисовку интерфейса
// подключим позже, когда появится переключатель языка.
function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('mc_lang', lang);
}

// Локаль для дат/времени по текущему языку (используется в fmtDate/nowStr).
function langLocale() {
  return { ru: 'ru-RU', en: 'en-US', he: 'he-IL' }[currentLang] || 'ru-RU';
}
